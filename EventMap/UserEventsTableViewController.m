#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.eventMapModel getNumberOfUserEvents];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"UserEventTableCell" forIndexPath:indexPath];
    
    // Configure the cell...
    
    NSString *name = [self.eventMapModel getUserEventNameAtIndex: indexPath.row];
    [[cell textLabel] setText: name];
    return cell;
}


